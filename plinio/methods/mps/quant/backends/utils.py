def binary_search(div, low, high, x):
    if high != low:
        mid = (low + high) // 2
        if x == mid * div:
            return mid
        if x < mid * div:
            return binary_search(div, low, mid, x)
        else:
            return binary_search(div, mid + 1, high, x)
    else:
        return low
